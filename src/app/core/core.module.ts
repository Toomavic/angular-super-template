const APP_MISC_PAGES = [P404Component, P500Component];
const APP_PROVIDERS = [];

@NgModule({
  declarations: [...APP_CONTAINERS, ...APP_MISC_PAGES],
  imports: [CommonModule, RouterModule, ...ANGULAR_MAT_MODULES],
  providers: []
})
export class CoreModule {
  constructor(@Optional() @SkipSelf() parentModule: CoreModule) {
    throwIfAlreadyLoaded(parentModule, 'CoreModule');
  }

  static forRoot(): ModuleWithProviders {
    return {
      ngModule: CoreModule,
      providers: [...APP_PROVIDERS]
    } as ModuleWithProviders;
  }
}
